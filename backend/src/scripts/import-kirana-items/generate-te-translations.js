function main() {
  const [inputPath, outputPath] = process.argv.slice(2);
  if (!inputPath || !outputPath) {
    throw new Error('Usage: generate-te-translations.js <input.json> <output.js>');
  }
  const data = JSON.parse(fs.readFileSync(inputPath, 'utf8'));

  const categories = {};
  const items = {};
  const missing = [];

  for (const c of data.categories) {
    const te = CATEGORY_TE[c.name];
    if (!te) {
      missing.push({ kind: 'category', id: c.id, name: c.name });
      continue;
    }
    categories[c.id] = te;
  }

  for (const it of data.items) {
    const te = ITEM_TE[it.name];
    if (!te) {
      missing.push({ kind: 'item', id: it.id, name: it.name });
      continue;
    }
    items[it.id] = te;
  }

  console.log(`Categories mapped: ${Object.keys(categories).length} / ${data.categories.length}`);
  console.log(`Items mapped:      ${Object.keys(items).length} / ${data.items.length}`);
  if (missing.length > 0) {
    console.log(`\nMissing translations (${missing.length}):`);
    for (const m of missing) console.log(`  ${m.kind}: "${m.name}" (${m.id})`);
  }

  const out =
    `// AUTO-GENERATED by generate-te-translations.js. Do not edit by hand.\n` +
    `// Translations are sourced from a hand-curated dictionary keyed by item/category name.\n` +
    `module.exports = ${JSON.stringify({ categories, items }, null, 2)};\n`;
  fs.writeFileSync(outputPath, out, 'utf8');
  console.log(`\nWrote ${outputPath}`);
  if (missing.length > 0) {
    process.exitCode = 2; // signal partial success
  }
}

main();
